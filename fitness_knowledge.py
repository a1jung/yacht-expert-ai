def get_fitness_routine(age: int, focus: str):
    # 나이 그룹 확인
    group = "청소년" if age in AGE_GROUPS["청소년"] else "성인"
    focus_key = focus.lower()
    
    # 루틴 선택
    routine = FITNESS_ROUTINES.get(focus_key)
    if routine:
        return routine[group]
    else:
        return {
            "message": "죄송합니다. 해당 부위/운동에 대한 데이터가 없습니다. '전신', '상체', '하체', '팔/다리', '인터벌', '코어/균형' 중 선택해주세요."
        }
